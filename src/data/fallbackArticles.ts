function daysAgoISO(days: number): string {
  const date = new Date();
  date.setDate(date.getDate() - days);
  return date.toISOString();
}

function placeholderImage(id: string): string {
  return `https://picsum.photos/seed/${encodeURIComponent(id)}/400/280`;
}

function createFallbackArticle(
  id: string,
  title: string,
  shortDescription: string,
  category: FallbackArticleCategory,
  readTime: number,
  articleUrl: string,
  daysAgo: number,
): FallbackArticle {
  return {
    id,
    title,
    shortDescription,
    category,
    readTime,
    articleUrl,
    imageUrl: placeholderImage(id),
    publishedDate: daysAgoISO(daysAgo),
  };
}

